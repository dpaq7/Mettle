// All themes (includes MCDM theme at the start for picker display)
export const allThemes: ThemeDefinition[] = [mcdmTheme, ...classThemes];

// Get theme by ID
export const getThemeById = (id: string): ThemeDefinition | undefined => {
  return allThemes.find((t) => t.id === id);
};

// Get default theme for a class
export const getDefaultThemeForClass = (heroClass: HeroClass): ThemeDefinition => {
  const theme = classThemes.find((t) => t.defaultForClass === heroClass);
  return theme || summonerTheme; // Fallback to Summoner theme
};

